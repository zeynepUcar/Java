public class Task9While {
    public static void main(String[] args) {
        //write a java program that iterates an array using while-loop

        int[] arr = {1, 23, 34, 45, 34, 645, 6, 45};
        int i = 0;
        while (i < arr.length){
            System.out.print(arr[i] + " ");
            i++;
        }


    }
}
