public class TwoDArray {
    public static void main(String[] args) {
        int[] apple = {3, 4, 5};
        int[] milk = {3, 3, 3};

        for (int i = 0; i < apple.length; i++) {
            System.out.print(apple[i] + " ");
        }
        System.out.println();
        for (int i = 0; i < milk.length; i++) {
            System.out.print(milk[i] + " ");
        }



    }
}

